package com.mashen.privilege.datarule;

/**
 * 自定义数据规则
 * @author liushd
 *
 */
public class CustomRule {
	private String  type;
	
	private String  value;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
